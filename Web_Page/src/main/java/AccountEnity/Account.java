package AccountEnity;

public class Account {
	private int id;
	private String name,emailId,password,address,contact;
	
	public Account() {
		super();
	}

	public Account(int id, String name, String emailId, String password, String address, String contact) {
		super();
		this.id = id;
		this.name = name;
		this.emailId = emailId;
		this.password = password;
		this.address = address;
		this.contact = contact;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	@Override
	public String toString() {
		return "Account [id=" + id + ", name=" + name + ", emailId=" + emailId + ", password=" + password + ", address="
				+ address + ", contact=" + contact + "]";
	}
	
	
	

}
